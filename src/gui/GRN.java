    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        // TODO add your handling code here:
        if (jTextField4.getText().isEmpty()) {

            jLabel32.setText("0.00");
            jLabel32.setForeground(Color.blue);

        } else {
            String total = jLabel36.getText();
            String payment = jTextField4.getText();

            Double balance = Double.parseDouble(payment) - Double.parseDouble(total);

            if (balance < 0) {
                jLabel32.setForeground(Color.red);
            } else {
                jLabel32.setForeground(Color.green);
            }

            jLabel32.setText(String.valueOf(df.format(balance)));
        }
    }//GEN-LAST:event_jTextField4KeyReleased

