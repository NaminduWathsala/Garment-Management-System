    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String supplierid = jLabel10.getText();
        String supplyid = jLabel13.getText();

        String count = jTextField1.getText().toLowerCase();
        String Price = jTextField2.getText();
        String Weight = jTextField3.getText();

        if (supplierid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select the supplier", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (supplyid.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select the product", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("(0)|([1-9][0-9]*)|(([1-9][0-9]*)[.]([0]*[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(Price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid buying price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("(0)|([1-9][0-9]*)|(([1-9][0-9]*)[.]([0]*[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(count).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Count or Yards", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("(0)|([1-9][0-9]*)|(([1-9][0-9]*)[.]([0]*[1-9][0-9]*))|([0][.]([0]*[1-9][0-9]*))").matcher(Weight).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Weight", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            boolean isFound = false;
            int x = -1;

            for (int i = 0; i < dtm.getRowCount(); i++) {
                String id = jTable1.getValueAt(i, 1).toString();

                if (id.equals(supplyid)) {
                    isFound = true;
                    x = i;
                    break;
                }
            }

            if (isFound) {
                int option = JOptionPane.showConfirmDialog(this, "This Supply is already added. Do yoou want to update?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {

                    double oldCount = Double.parseDouble(jTable1.getValueAt(x, 4).toString());
                    double finalCount = oldCount + Double.parseDouble(count);

                    double oldWeight = Double.parseDouble(jTable1.getValueAt(x, 5).toString());
                    double finalWeight = oldWeight + Double.parseDouble(Weight);

                    jTable1.setValueAt(String.valueOf(finalCount), x, 4);
                    jTable1.setValueAt(String.valueOf(finalWeight), x, 5);
                    jTable1.setValueAt(Double.parseDouble(Price), x, 6);

                    double updateItemTotal = finalCount * Double.parseDouble(Price);
                    jTable1.setValueAt(String.valueOf(updateItemTotal), x, 7);

                    updateTotal();

                }

                resetFields();

            } else {

                Vector v = new Vector();
                v.add(jLabel18.getText());
                v.add(supplyid);
                v.add(jLabel20.getText());
                v.add(jLabel23.getText());
                v.add(Double.parseDouble(count));
                v.add(Double.parseDouble(Weight));
                v.add(Double.parseDouble(Price));
                double itemtotal = Double.parseDouble(count) * Double.parseDouble(Price);
                v.add(df.format(itemtotal));

                dtm.addRow(v);

                updateTotal();
//
                resetFields();
                JOptionPane.showMessageDialog(this, "Product added to the GRN", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

